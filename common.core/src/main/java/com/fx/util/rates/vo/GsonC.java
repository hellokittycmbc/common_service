package com.fx.util.rates.vo;

import java.util.Date;

public class GsonC {
	private double now;
	private String date;
	private String time;
	private double buy;
	private double sale;
	private String from_cn;
	private String to_cn;
	public double getNow() {
		return now;
	}
	public void setNow(double now) {
		this.now = now;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public double getBuy() {
		return buy;
	}
	public void setBuy(double buy) {
		this.buy = buy;
	}
	public double getSale() {
		return sale;
	}
	public void setSale(double sale) {
		this.sale = sale;
	}
	public String getFrom_cn() {
		return from_cn;
	}
	public void setFrom_cn(String from_cn) {
		this.from_cn = from_cn;
	}
	public String getTo_cn() {
		return to_cn;
	}
	public void setTo_cn(String to_cn) {
		this.to_cn = to_cn;
	}
	
}
